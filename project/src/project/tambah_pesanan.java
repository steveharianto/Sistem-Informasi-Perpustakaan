    public void refreshBarang(){
        currentBarang = barangList.get(cbBarang.getSelectedIndex());
        txtIDBarang.setText(String.valueOf(currentBarang.getId()));
        txtHargaBarang.setText(String.valueOf(currentBarang.getHarga()));
    }
    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        Object[] columnNames = {"ID", "Nama", "Harga", "Jumlah"};
        model.setColumnIdentifiers(columnNames);

        int total = 0;
        for (Barang barang : notaList) {
            Object[] row = {barang.getId(), barang.getNama(), barang.getHarga(), barang.getJumlah()};
            model.addRow(row);
            total += barang.getHarga() * barang.getJumlah();
        }
        
        // Set Total Harga
        txtTotalHarga.setText(String.valueOf(total));
    }
    
    // Get all Barang records from the database
    public static ArrayList<Barang> getAllBarang(Connection connection) throws SQLException {
        ArrayList<Barang> barangList = new ArrayList<>();

        String query = "SELECT * FROM Barang";
        PreparedStatement statement = connection.prepareStatement(query);
        ResultSet resultSet = statement.executeQuery();

        while (resultSet.next()) {
            int id = resultSet.getInt("id");
            String nama = resultSet.getString("nama");
            int harga = resultSet.getInt("harga");
            int jumlah = resultSet.getInt("jumlah");

            Barang barang = new Barang(id, nama, harga, jumlah);
            barangList.add(barang);
        }

        resultSet.close();
        statement.close();

        return barangList;
    }

