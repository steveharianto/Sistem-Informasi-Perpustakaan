    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            // Insert Customer
            Member m = new Member(1, txtNamaCustomer.getText());
            int member_id = m.insert(DBConnection.getConnection());
            
            // Insert Pesanan  | int id, int id_user, Date tanggal
            java.util.Date currentDate = new java.util.Date();
            long currentTimeMillis = currentDate.getTime();
            java.sql.Date sqlDate = new java.sql.Date(currentTimeMillis);
            Pesanan p = new Pesanan(1,member_id, sqlDate, Integer.valueOf(txtTotalHarga.getText()));
            int pesanan_id = p.insert(DBConnection.getConnection());
            
            for(Barang b : notaList){
                // int id, int id_pesanan, int id_barang, int jumlah, int subtotal
                Pesanan_item pi = new Pesanan_item(1, pesanan_id, b.getId(), Integer.valueOf(txtJumlahBarang.getText()),(Integer.valueOf(txtJumlahBarang.getText())* b.getHarga()));
                pi.insert(DBConnection.getConnection());
            }
            
            
            JOptionPane.showMessageDialog(null, "Pesanan Berhasil Dimasukkan", "Information", JOptionPane.INFORMATION_MESSAGE);
            main a = new main ();
            a.setVisible(true);
            this.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(tambah_pesanan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

